<p>il a été convenu ce qui suit :</p>
<p><font class="underline">Art. 1</font><br>L'Entreprise accepte de prendre en stage “l'Etudiant” et désigne un membre de son personnel comme maître de stage.</p>
<p><font class="underline">Art. 2</font><br>Le stage débute le 1er février 2016 et prend fin le 21 mai 2016 (moyennant accord des différentes parties, cette période pourrait être prolongée).
Les prestations s'effectuent 5 jours par semaine aux heures prévues par l'Entreprise. Le calendrier des congés scolaires n’est pas d’application. L’Etudiant doit effectuer ses prestations dans les locaux de l’Entreprise.</p>
<p><font class="underline">Art. 3</font><br>L'Entreprise informera Madame Colette De Muylder ou Monsieur Bernard Henriet, responsables des stages au sein de l’Institut de toute absence de “l'Etudiant” durant les jours prévus pour l'exécution du stage.</p>
<p><font class="underline">Art. 4</font><br>Il n'existe pas de convention de travail entre l'Etudiant et l'Entreprise, celui-ci continue à relever de l'Institut auquel il appartient. Les conséquences en sont que:</p>
<ol>
	<li>Le stagiaire n'est pas soumis à la législation relative à la sécurité sociale. Aucune cotisation ne peut donc être mise à charge de l'Entreprise.</li>
	<li>La législation concernant les accidents de travail et les accidents survenus sur le chemin à l'aller et au retour du travail, de même celle concernant les maladies professionnelles ne sont pas d'application pour l'Etudiant.</li>
</ol>
<p>L'établissement d'enseignement est tenu de prendre toutes les dispositions pour couvrir les risques d'accidents et de maladie sans qu'il en résulte la moindre charge pour l'Entreprise.</p>
<p>L'Institut prend l'engagement de renoncer à toute action en dommages, soit personnelle, soit par l'intermédiaire d'un assureur, pour les accidents dont l'étudiant aura pu être victime.</p>

<p><font class="underline">Art. 5</font><br>L'étudiant ne peut recevoir de rémunérations.</p>
<p><font class="underline">Art. 6</font><br>L'Entreprise s'engage à permettre au stagiaire d'élaborer, à partir du travail réalisé, un rapport écrit qui sera défendu par l'étudiant devant un jury; un membre de l'Entreprise sera invité à assister à cette défense.<br>
Avant toute officialisation du rapport, l'étudiant stagiaire le soumet au maître de stage et se conforme aux recommandations de ce dernier.<br>
L'étudiant stagiaire s'engage à ne pas divulguer, même après le stage, les informations confidentielles de l'entreprise auxquelles il aurait pu avoir accès.<br>
Les trois parties reconnaissent que tous les droits intellectuels sur les programmes créés pendant le stage, ainsi que le droit de commercialisation de ces programmes, appartiennent entièrement et uniquement à l'Entreprise.</p>
<p><font class="underline">Art. 7</font><br>L'Institut désignera un professeur qui se chargera durant le stage de prendre contact avec l'entreprise afin de suivre l'évolution du travail réalisé.</p>
